public class TablePrinter
	 {
   		 public static void main(String[] args)
		 {
        		int number = 5; // You can change this to any number

        		System.out.println("Table of " + number + ":");
       			 for (int i = 1; i <= 10; i++)
			 {
           		 System.out.println(number + " x " + i + " = " + (number * i));
      			 }
   		 }
	}
